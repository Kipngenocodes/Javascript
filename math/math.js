// The JavaScript math object provides properties and methods for mathematical constants and functions.
// All the properties and methods of Math are static and can be called by using Math as an object without creating it.

// JavaScript Math Properties
// Euler's constant and the base of natural logarithms, approximately 2.718.
console.log("Euler's constant (e):", Math.E);

// The natural logarithm of 10, approximately 2.302.
console.log("Natural logarithm of 10:", Math.LN10);

// The natural logarithm of 2, approximately 1.442.
console.log("Natural logarithm of 2:", Math.LN2);

// The base-10 logarithm of 1, approximately 0.434.
console.log("Base-10 logarithm of 1:", Math.LOG10E);

// The base-2 logarithm of 1, approximately 1.585.
console.log("Base-2 logarithm of 1:", Math.LOG2E);

// Pi, approximately 3.141.
console.log("Pi:", Math.PI);    

// Square root of 2, approximately 1.414.
console.log("Square root of 2:", Math.SQRT2);

// sqrt1_2 is the reciprocal of the square root of 2, approximately 0.707.
console.log("Reciprocal of the square root of 2:", Math.SQRT1_2);


// JavaScript Math Methods
// The Math.abs() method returns the absolute value of a number.
console.log("Absolute value of -5:", Math.abs(-5)); // Output: 5

// The Math.ceil() method returns the smallest integer greater than or equal to a number.
console.log("Ceiling of 2.1:", Math.ceil(2.1)); // Output: 3

// The Math.floor() method returns the largest integer less than or equal to a number.
console.log("Floor of 2.9:", Math.floor(2.9)); // Output: 2 

// The Math.round() method rounds a number to the nearest integer.
console.log("Rounded value of 2.5:", Math.round(2.5));

// The Math.random() method returns a floating-point, pseudo-random number in the range [0, 1).
console.log("Random number:", Math.random());   

// The Math.pow() method returns the base to the exponent power.
console.log("2 raised to the power of 3:", Math.pow(2, 3));

// The Math.sqrt() method returns the square root of a number.
console.log("Square root of 16:", Math.sqrt(16));       

// The Math.min() method returns the smallest of zero or more numbers.
console.log("Minimum of 2, 5, 3:", Math.min(2, 5, 3));  

// The Math.max() method returns the largest of zero or more numbers.   
console.log("Maximum of 2, 5, 3:", Math.max(2, 5, 3));

// The Math.log() method returns the natural logarithm of a number.
console.log("Natural logarithm of 10:", Math.log(10));  

// The Math.exp() method returns the base of the natural logarithm e raised to the power of a number.
console.log("e raised to the power of 2:", Math.exp(2));