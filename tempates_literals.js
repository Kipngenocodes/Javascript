/*he template literals are introduced in ES6 to customize the string dynamically.
 The template literals allow you to add variables or expressions into the string, 
and the string changes according to the variables and expression value changes.
*/
// syntax for templates literals : let str = `Hi ${name}`;


// Example 1: Using template literals to create a string with variables
let name = "John";
let greeting = `Hello, ${name}!`;
console.log(greeting);  // Output: Hello, John!